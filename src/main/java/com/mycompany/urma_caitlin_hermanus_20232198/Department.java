    //delete button
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        //if no row was selected
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // this gets the selected row and deletes it
        int fa_Delete = (int) jTable1.getValueAt(selectedRow, 0);

        Connection conn = null;
        PreparedStatement pstmt = null;

        //error handling
        try {
            String connStr = "jdbc:sqlite:C:\\Users\\Caitlin\\SQL lite\\sqlite-tools-win32-x86-3430000\\URMS_DB.db";
            Class.forName("org.sqlite.JDBC");
            conn = DriverManager.getConnection(connStr);

            if (conn != null) {
                System.out.println("Connected");

                // Prepare the DELETE query to remove the record from Assignments
                String query = "DELETE FROM Assignments WHERE assignment_id = ?";
                pstmt = conn.prepareStatement(query);
                pstmt.setInt(1, fa_Delete);

                // Execute the query
                int rowsDeleted = pstmt.executeUpdate();

                if (rowsDeleted > 0) {
                    // If deletion is successful, remove the row from the jTable1
                    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                    model.removeRow(selectedRow);
                    JOptionPane.showMessageDialog(this, "Assignment deleted successfully.");
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to delete Assignment.", "Error", JOptionPane.ERROR_MESSAGE);
                }

                // Close 
                pstmt.close();
            } else {
                System.out.println("Connection failed");
            }

        } catch (Exception e) {
            // exceptions
            System.out.println("Error: " + e.getMessage());
        } finally {
            // Close the connection in the 'finally' block to ensure it's always closed
            try {
                if (conn != null) {
                    conn.close();
                    System.out.println( "Connection closed");
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error closing connection: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    //upload button
    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Select File to Upload");

        //this is used to bring a file open and choose a file to upload
        int result = fileChooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();

            //file path
            String uploadDirectory = "E:\\Class\\Semester 2\\JD522\\SA\\FileForSA"; 

            File uploadLocation = new File(uploadDirectory);
            if (!uploadLocation.exists()) {
                uploadLocation.mkdirs(); 
            }

            String filePath = uploadDirectory + selectedFile.getName();

            //error handling
            try {
                Files.copy(selectedFile.toPath(), Paths.get(filePath), StandardCopyOption.REPLACE_EXISTING);
                JOptionPane.showMessageDialog(this, "File uploaded successfully ");
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Error uploading file: " + ex.getMessage());
            }
        } else if (result == JFileChooser.CANCEL_OPTION) {
            JOptionPane.showMessageDialog(this, "Canceled upload.");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

