    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        //creating the connection
        try {
            String connStr = "jdbc:sqlite:C:\\Users\\Caitlin\\SQL lite\\sqlite-tools-win32-x86-3430000\\URMS_DB.db";
            Class.forName("org.sqlite.JDBC");
            Connection conn = DriverManager.getConnection(connStr);

            if (conn != null) {
                System.out.println("Connected");

                //query to get assessments
                String query = "SELECT * FROM Assignments";
                PreparedStatement statement = conn.prepareStatement(query);
                ResultSet rs = statement.executeQuery();

                // holds the data for the JTable
                DefaultTableModel model = new DefaultTableModel();
                model.addColumn("Assignment ID");
                model.addColumn("Assignment Name");
                model.addColumn("Department Name");
                model.addColumn("Department Head");
                model.addColumn("Assignment Date");

                //  retrieves data
                while (rs.next()) {
                    int assignmentID = rs.getInt("assignment_id");
                    String assignmentName = rs.getString("assignment_name");
                    String departmentName = rs.getString("department_name");
                    String departmentHead = rs.getString("department_head");
                    String assignmentDate = rs.getString("assignment_date");

                    model.addRow(new Object[]{assignmentID, assignmentName, departmentName, departmentHead, assignmentDate});
                }

                // Set the DefaultTableModel to the jTable1
                jTable1.setModel(model);

                rs.close();
                statement.close();
                conn.close();
            } else {
                JOptionPane.showMessageDialog(this, "Connection failed");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

